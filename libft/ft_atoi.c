#include "libft.h"

int     ft_atoi(const char *str)
{
    int i;
    int res;

    i = 0;
    res = 0;
    if (str[i] == '-' || str[i] == '+')
        i++;
    while (str[i] >= 48 && str[i] <= 57)
    {
        res += str[i] - '0';
        if (str[i + 1] >= 48 && str[i + 1] <= 57)
            res *= 10;
        i++;
    }
    if(str[0] == '-')
        res *= -1;
    return (res);
}